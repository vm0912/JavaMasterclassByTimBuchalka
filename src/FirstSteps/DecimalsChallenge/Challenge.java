package FirstSteps.DecimalsChallenge;

//converting pounds to kilograms
public class Challenge {

    public static void main(String[] args) {
        double poundsInKgs = 0.45359237;
        double noOfPounds = 100;
        double noOfKgs = noOfPounds * poundsInKgs ;
        System.out.println(noOfPounds+" pounds is equal to "+ noOfKgs + " kgs");
    }

}
